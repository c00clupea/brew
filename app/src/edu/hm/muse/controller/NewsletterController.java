package edu.hm.muse.controller;

import edu.hm.muse.domain.Newsletter;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.servlet.ModelAndView;

import javax.annotation.Resource;
import javax.sql.DataSource;
import java.util.ArrayList;
import java.util.List;

@Controller
public class NewsletterController {
	private JdbcTemplate jdbcTemplate;

	@Resource(name = "dataSource")
	public void setDataSource(final DataSource dataSource) {
		jdbcTemplate = new JdbcTemplate(dataSource);
	}

	@RequestMapping(value = "/newsletter.secu", method = RequestMethod.GET)
	public ModelAndView showNewsletter(@RequestParam(value = "id", required = false) final String id) {

		final ModelAndView mv = new ModelAndView("newsletter");

		String sql = "select * from NEWSLETTER";
		final List<Newsletter> allNewsletters = jdbcTemplate.query(sql, new NewsletterMapper());
		mv.addObject("allNewsletters", allNewsletters);

		List<Newsletter> newsletters = new ArrayList<Newsletter>();
		boolean somethingGoneWrong = false;
		try {
			sql = "select * from NEWSLETTER where id = " + id;
			newsletters = jdbcTemplate.query(sql, new NewsletterMapper());
			if (newsletters.size() == 0)
				somethingGoneWrong = true;
		} catch (final Exception x) {
			somethingGoneWrong = true;
		}

		if (somethingGoneWrong) {
			sql = "select * from NEWSLETTER";
			newsletters = jdbcTemplate.query(sql, new NewsletterMapper());
		}

		mv.addObject("newsletters", newsletters);
		return mv;
	}
}
